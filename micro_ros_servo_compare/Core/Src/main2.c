static int bla = 0;
static int bli = 0;

void subscription_callback(const void *msgin)
{
  const midpoint_message__msg__MidpointMessage *msg = (const midpoint_message__msg__MidpointMessage *)msgin;

  int x_value = map(msg->midpoint_x, 1, 640, 1100, 7500);
  int y_value = map(msg->midpoint_y, 1, 480, 1100, 6500);
  
  bla++;
  bli++;
  
  x_value = map(bla, 1, 640, 1100, 7500);
  y_value = map(bli, 1, 480, 1100, 6500);

  //TIM2->CCR1 = bla++;
  //TIM2->CCR2 = bli++;

  TIM2->CCR1 = x_value;
  TIM2->CCR2 = y_value;

  midpoint_message__msg__MidpointMessage callback_msg;
  callback_msg.midpoint_x = x_value;
  callback_msg.midpoint_y = y_value;

  rcl_ret_t ret = rcl_publish(&publisher, (const void *)&callback_msg, NULL);
  if (ret != RCL_RET_OK)
  {
    printf("Error publishing (line %d)\n", __LINE__);
  }
}

void StartDefaultTask(void *argument)
{
  rmw_uros_set_custom_transport(
      true,
      (void *)&huart2,
      cubemx_transport_open,
      cubemx_transport_close,
      cubemx_transport_write,
      cubemx_transport_read);

  rcl_allocator_t freeRTOS_allocator = rcutils_get_zero_initialized_allocator();
  freeRTOS_allocator.allocate = microros_allocate;
  freeRTOS_allocator.deallocate = microros_deallocate;
  freeRTOS_allocator.reallocate = microros_reallocate;
  freeRTOS_allocator.zero_allocate = microros_zero_allocate;

  if (!rcutils_set_default_allocator(&freeRTOS_allocator))
  {
    printf("Error on default allocators (line %d)\n", __LINE__);
  }

  // micro-ROS app

  rclc_support_t support;
  rcl_allocator_t allocator;
  rcl_node_t node;

  rcl_subscription_t subscriber;

  allocator = rcl_get_default_allocator();

  // create init_options
  rclc_support_init(&support, 0, NULL, &allocator);

  // create node
  rclc_node_init_default(&node, "servo_mcu", "", &support);

  // create publisher
  rclc_publisher_init_default(
      &publisher,
      &node,
      ROSIDL_GET_MSG_TYPE_SUPPORT(midpoint_message, msg, MidpointMessage),
      "midpoint_callback");

  // Initialize subscriber
  rcl_ret_t rc = rclc_subscription_init_default(
      &subscriber,
      &node,
      ROSIDL_GET_MSG_TYPE_SUPPORT(midpoint_message, msg, MidpointMessage),
      "midpoint_coordinates");

  rclc_executor_t executor;
  rclc_executor_init(&executor, &support.context, 1, &allocator);
  rclc_executor_add_subscription(&executor, &subscriber, &midpoint_msg, &subscription_callback, ON_NEW_DATA);

  for (;;)
  {
    rclc_executor_spin_some(&executor, RCL_MS_TO_NS(100));

    osDelay(10);
  }
}

